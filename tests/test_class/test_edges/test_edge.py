    n1 = Node(x, y)
    n2 = Node(x + dx, y + dy)
    edge = Edge(n1, n2)
    [e1, e2] = edge.split()

    if dx > 0:
        assert e1[0] == n1
        assert e1[1] == edge.center
        assert e2[0] == edge.center
        assert e2[1] == n2
    elif dx < 0:
        assert e2[1] == n1
        assert e2[0] == edge.center
        assert e1[1] == edge.center
        assert e1[0] == n2
    else:
        if dy > 0:
            assert e1[0] == n1
            assert e1[1] == edge.center
            assert e2[0] == edge.center
            assert e2[1] == n2
        elif dy < 0:
            assert e2[1] == n1
            assert e2[0] == edge.center
            assert e1[1] == edge.center
            assert e1[0] == n2


